package bridgeLAbz;

public class StaticVarables {
	static String str = "bridgelabz";// static variable.

	static void methods() {// this the method body.
		System.out.println(str);// group of statements can perform single operation.
	}

	// this are the main method block.
	public static void main(String[] args) {
		methods();
	}

}
